    def colorEstado(self):
        self.listadeventanasPositivas = []
        self.listadevantanasNegativas = []
        from VentanitaPositivo import VentanaPositivo
        temperatura = self.cvp.temperatura()[0]
        self.temperaturaLCD(temperatura)
        self.etiquetasEnBlanco()
        desocupado = 0
        positivo = 0
        negativo = 0
        proceso = 0
        ############## consulto datos ############
        datos = self.cvp.cambiar()
        for j in datos:
                color = j [0]
                fecha=j[1]
                hora = str(j[2])
                self.valorAlarma = j[3]
                self.celdas[j[4]-1].setStyleSheet(_fromUtf8("border-image: url(" + self.direccionDeImagenes +color+") 0 0 0 0 stretch stretch;"))
              ################# manejo celdas en alarma ####################
                self.manejoAlarma(j[4]-1,self.valorAlarma,color)
             ############## si  esta ocupada la celda, muestra los labels de fecha de ingreso y tiempo trascurrido #######
                if color != "cGris" :
                    self.etiquetas[(j[4]-1) * 2].setText(fecha)
                    self.etiquetas[((j[4]-1) * 2) + 1].setText(hora)
                    if self.usuario != " ":
                        self.celdas[j[4]-1].setEnabled(True)
                        ########### depende el color se actualiza el contador  y el estado de las celdas ###########
                if color == "cGris":
                    #self.celdas[j[4]-1].setEnabled(False)
                    if self.celdas[j[4]-1] in self.diccionarioPositivo:
                        self.diccionarioPositivo.remove(self.celdas[j[4]-1])
                    desocupado += 1

                if color == "cVerde":

                    if self.celdas[j[4]-1] not in self.diccionarioNegativo:
                        if self.cvp.estadoAceptado(j[5]) == False:

                            texto ="    Negativo detectado en celda  " + str( j[4])
                            colorNegativo = "border-color: rgb(0, 255, 0);background-color: rgb(0, 255  0);"
                            van= VentanaPositivo(j[4], texto, colorNegativo)
                            van.showFullScreen()
                            self.listadevantanasNegativas.append(van)
                        self.diccionarioNegativo.append(self.celdas[j[4]-1])
                    negativo +=1

                if color == "cRojo":
                    if self.celdas[j[4]-1] not in self.diccionarioPositivo:
                        if self.cvp.estadoAceptado(j[5]) == False:

                            texto ="    Positivo detectado en celda : " + str( j[4])
                            colorPositivo = "border-color: rgb(255, 0, 0);background-color: rgb(255, 0, 0);"
                            vap= VentanaPositivo(j[4], texto, colorPositivo)
                            vap.showFullScreen()
                            self.listadeventanasPositivas.append(vap)
                        self.diccionarioPositivo.append(self.celdas[j[4]-1])
                    positivo+=1

                if color == "cAmarillo":
                    proceso += 1
                self.label_109.setText("Total ocupados = "+str(negativo+positivo+proceso))
                self.a1_3.setText(str(negativo))
                self.a1_2.setText(str(positivo))
                self.a1_4.setText(str(desocupado))
                self.a1_5.setText(str(proceso))

    def manejoAlarma(self,celda,valorAlarma,color):
        if valorAlarma == 1 :
                if celda not in self.celdasEnAlarma :
                        self.celdasEnAlarma[celda] = color
                        self.celdasEnFlag[celda] = False
                else :
                    self.celdasEnAlarma[celda] = color
                    self.celdasEnFlag[celda] = True
        if self.valorAlarma == 0:
                if celda in self.celdasEnAlarma :
                    del self.celdasEnAlarma[celda]

    def upiteoAlarma(self):

            for i in self.celdasEnAlarma :
                if self.celdasEnFlag[i] == False:
                    self.celdas[i].setStyleSheet(_fromUtf8("border-image: url("+self.direccionDeImagenes+self.celdasEnAlarma[i]+") 0 0 0 0 stretch stretch;"))
                    self.celdasEnFlag[i] = True
                else:
                    self.celdas[i].setStyleSheet(_fromUtf8("border-image: url(" + self.direccionDeImagenes + "cAlarma) 0 0 0 0 stretch stretch;"))
                    self.celdasEnFlag[i] = False

