    def temporizador(self, valor=1000):
        self.timer= QtCore.QTimer(self)
        self.timer.timeout.connect(self.refrescar)
        if self.timer.isActive() == False:
            self.timer.start(valor)
        else:
            self.timer.setInterval(valor)

    def refrescar(self):

        if self.c!=30 and len(self.celdasEnAlarma)!= 0 :
            self.upiteoAlarma()
        elif self.c == 30:
            self.colorEstado()
            self.c=0
        self.c = self.c + 1

